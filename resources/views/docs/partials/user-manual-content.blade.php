<h2>3. Customer Guide</h2>
<ol>
    <li>Browse meals on the home page or <strong>/meals</strong>.</li>
    <li>Add items to your cart and go to checkout.</li>
    <li>Select a delivery address (city &amp; district in Tanzania).</li>
    <li>Pay with M-Pesa, Tigo Pesa, Airtel Money, card, or cash on delivery.</li>
    <li>Track orders from <strong>My Orders</strong> on your dashboard.</li>
    <li>Leave a review after delivery.</li>
</ol>

<h3>Order statuses</h3>
