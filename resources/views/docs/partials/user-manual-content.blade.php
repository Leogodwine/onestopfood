<h2>2. Getting Started</h2>

<h3>2.1 Create an Account</h3>
<table>
    <thead><tr><th>Method</th><th>Best for</th></tr></thead>
    <tbody>
        <tr><td>Email &amp; password</td><td>All roles — traditional registration</td></tr>
        <tr><td>Google sign-in</td><td>Quick sign-up — no password required</td></tr>
        <tr><td>Facebook sign-in</td><td>Quick sign-up — no password required</td></tr>
    </tbody>
</table>

<p><strong>Email registration:</strong> Login → Register → fill name, email, phone, role, password → Create Account.</p>

<p><strong>Social sign-in:</strong></p>
<ol>
    <li>Click Google or Facebook on the login page.</li>
    <li>Authorize with your social account (name and email are captured automatically).</li>
    <li>On <strong>Complete Your Sign Up</strong>, enter your phone number.</li>
    <li>Receive a 6-digit code by email or phone and verify.</li>
</ol>
<blockquote>Social accounts do not use a password. Always sign in with Google or Facebook.</blockquote>

<h3>2.2 Roles</h3>
<table>
    <thead><tr><th>Role</th><th>What you can do</th></tr></thead>
    <tbody>
