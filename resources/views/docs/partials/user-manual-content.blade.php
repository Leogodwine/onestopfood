<h2>4. Chef Guide</h2>
<h3>Registration</h3>
<ol>
    <li>Click <strong>Become a Chef</strong> on the login page (or register with Chef role).</li>
    <li>Complete all six verification tabs at <strong>/verify</strong>.</li>
    <li>Wait for admin approval before accessing the full chef dashboard.</li>
</ol>

<h3>Verification sections</h3>
<ol>
    <li><strong>Identity &amp; Contact</strong> — DOB, NIDA ID, nationality, selfie</li>
    <li><strong>Kitchen Location</strong> — Address, city, district, kitchen type, photos (min. 2 for home kitchens)</li>
    <li><strong>Experience &amp; Skills</strong> — Bio, experience, specialties</li>
    <li><strong>Legal Documents</strong> — Food safety, licenses, permits</li>
    <li><strong>Payout Details</strong> — Bank account information</li>
    <li><strong>Compliance</strong> — Terms, code of conduct, declarations</li>
</ol>

<h3>After approval</h3>
<p>Create meals, accept orders, update status (Preparing → Ready), assign travelers, and view earnings from your chef dashboard.</p>

<h2>5. Traveler / Dropper Guide</h2>
<ol>
    <li>Register as Traveler and complete verification (identity, address, vehicle, insurance, banking).</li>
    <li>After approval, toggle online to receive delivery assignments.</li>
    <li>Pick up orders from chefs and deliver to customers.</li>
    <li>Update status: Picked up → Delivered.</li>
</ol>

<h2>6. Account &amp; Security</h2>
<ul>
    <li>Email accounts: minimum 8-character password; use Forgot Password if needed.</li>
    <li>Social accounts: phone + OTP verification required; no password.</li>
    <li>Edit profile name and photo from Dashboard → Profile.</li>
</ul>

