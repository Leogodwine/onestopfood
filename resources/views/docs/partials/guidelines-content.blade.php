<p class="lead">These guidelines define the rules and standards for all users of the One Stop Food platform. Chefs and travelers must accept them during verification.</p>

<h2>1. General Platform Rules</h2>

<h3>Eligibility</h3>
<ul>
    <li>Must be <strong>18 years or older</strong> to register as chef or traveler.</li>
    <li>Provide accurate, truthful information at all times.</li>
    <li>One active account per person per role unless admin-approved.</li>
</ul>

<h3>Prohibited conduct</h3>
<ul>
    <li>Fraud, fake orders, or payment manipulation</li>
    <li>Harassment, abuse, or discrimination</li>
    <li>Unsafe, misrepresented, or unlicensed food sales</li>
    <li>Off-platform payment solicitation</li>
    <li>False verification documents or impersonation</li>
</ul>
<p><strong>Consequences:</strong> Warning, suspension, or permanent removal.</p>

<h2>2. Customer Guidelines</h2>
<ul>
    <li>Provide correct delivery addresses including city and district.</li>
    <li>Be available to receive orders or designate a recipient.</li>
    <li>Write honest reviews; use support for disputes.</li>
    <li>Avoid repeated frivolous cancellations.</li>
</ul>

<h2>3. Chef Guidelines</h2>

<h3>Professional standards</h3>
<ul>
    <li>Maintain a clean, safe kitchen environment.</li>
    <li>Use fresh ingredients and proper food handling.</li>
    <li>Accurately describe meals, ingredients, and allergens.</li>
    <li>Honor accepted orders within reasonable time.</li>
</ul>

<h3>Kitchen requirements</h3>
<table>
    <thead><tr><th>Requirement</th><th>Detail</th></tr></thead>
    <tbody>
        <tr><td>Kitchen type</td><td>Home, commercial, or shared — must be declared</td></tr>
        <tr><td>Proof of kitchen</td><td>Valid supporting document required</td></tr>
        <tr><td>Kitchen photos</td><td>Minimum 2 for home kitchens</td></tr>
        <tr><td>Location</td><td>Accurate street, city, and district in Tanzania</td></tr>
    </tbody>
</table>

<h3>Legal documents</h3>
<p>Upload valid food safety certificates, business licenses, food handling permits, and health inspection documents where applicable. <strong>False documents result in immediate rejection and possible permanent ban.</strong></p>

<h2>4. Traveler / Dropper Guidelines</h2>
<ul>
    <li>Handle food carefully; use insulated bags when possible.</li>
    <li>Follow traffic laws; deliver only to the order address.</li>
    <li>Maintain valid license, vehicle insurance, and ownership proof.</li>
    <li>Toggle online/offline status honestly.</li>
    <li>For cash on delivery, collect the exact platform amount only.</li>
</ul>

<h2>5. Verification &amp; Approval</h2>
<ol>
    <li>Register as Chef or Traveler.</li>
    <li>Complete the verification form at <strong>/verify</strong>.</li>
    <li>Admin reviews documents in the Verifications queue.</li>
    <li>Account is approved, rejected, or sent back for more information.</li>
</ol>

<h2>6. Order &amp; Delivery Policy</h2>
<p><strong>Flow:</strong> Customer orders → Chef accepts &amp; prepares → Ready → Traveler delivers → Delivered → Review.</p>
<p>Refunds and cancellations are handled by admin through the Finance module based on order status and payment method.</p>

<h2>7. Data Privacy</h2>
<p>The platform collects name, email, phone, addresses, verification documents, and order history for service delivery and security. Documents are accessed only by authorized admins. Social sign-in never stores your Google/Facebook password.</p>

